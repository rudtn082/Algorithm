package Kruskal;

public class Edge {
	// 입력시 필요
	private int Start;
	private int End;
	private int Weight;
	
	// get,set
	public int getStart() {
		return Start;
	}
	public void setStart(int start) {
		Start = start;
	}
	public int getEnd() {
		return End;
	}
	public void setEnd(int end) {
		End = end;
	}
	public int getWeight() {
		return Weight;
	}
	public void setWeight(int weight) {
		Weight = weight;
	}
}
